// Open modal function àºàº²àº™à»€àº›àºµàº”àº«àº™à»‰àº² POPUP
function openModal(itemId) {
    const item = menuData[itemId];
    if (!item) return;

    document.getElementById('modalImage').src = item.image;
    document.getElementById('modalTitle').textContent = item.title;
    document.getElementById('modalPrice').textContent = item.price;
    document.getElementById('modalDescription').textContent = item.description;

    // Create details HTML
    const detailsHTML = item.details.map(detail =>
        `<div class="detail-item">
                    <span class="detail-icon">${detail.icon}</span>
                    <span>${detail.text}</span>
                </div>`
    ).join('');

    document.getElementById('modalDetails').innerHTML = detailsHTML;

    // Show modal àºªàº°à»àº”àº‡ modal
    document.getElementById('menuModal').classList.add('show');
    document.body.style.overflow = 'hidden'; // Prevent scrolling
}

// Close modal function àºàº±àº‡àºŠàº»àº™àºàº²àº™àº›àº´àº”àº«àº™à»‰àº² popup
function closeModal() {
    document.getElementById('menuModal').classList.remove('show');
    document.body.style.overflow = 'auto'; // Enable scrolling
}

// Close modal when clicking outside àºàº±àº‡àºŠàº»àº™àºàº²àº™àº›àº´àº”àº«àº™à»‰àº² popup àº—àº±àº‡àº™àº­àº
function closeModalOnOutside(event) {
    if (event.target.id === 'menuModal') {
        closeModal();
    }
}

// Close modal with Escape key
document.addEventListener('keydown', function (event) {
    if (event.key === 'Escape') {
        closeModal();
    }
});

